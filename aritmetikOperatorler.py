'''
= Atama operatörü
+ Toplama operatörü
- Çıkarma operatörü
* Çarpma operatörü
/ Bölme operatörü
// Tam Bölme operatörü
% Kalan operatörü
** Üst Alma operatörü
'''
sayi1 = 4 # = atama opearatörüdür, soldaki değer sağdaki değişkene aktarıldı
sayi2 = 3
sonuc = sayi1 + sayi2 # + ile toplama işlemi yapıldı, değer = ile sonuc değişkenine aktarıldı
print(sonuc)
print(sayi1 - sayi2)
print(sayi1 * sayi2)
print(sayi1 / sayi2) # sonucu float cinsinden hesaplar
print(sayi1 // sayi2) # ondalıklı kısmı atıp sonucu integer olarak hesaplar
print(sayi1 % sayi2) # kalan değeri hesaplar
print(sayi1 ** sayi2) # üst değerini hesaplar

#listeler birden fazla değer alabilen değişkenlerdir. 
#Köşeli parantez ile tanımlanırlar.
meyveler = ["elma","nar","kiraz","karpuz", "muz"]
print(meyveler)
#for döngüsü ile bir liste içindeki tüm elemanlara sıra ile ulaşılabilir
for meyve in meyveler:
    print(meyve)
#listelerin 0 dan başlayan index numarası vardır. Bu numaralar ile liste elemanlarına erişilir.
print(meyve[2])